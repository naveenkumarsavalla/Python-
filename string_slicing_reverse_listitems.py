#String Slicing
name="Naveen"
print(name[1:5])
print(name[:2])
print(name[5:])
print(name[:5])
print(name[:])

#Neghative index
a="Lohith"
print(a[-5:-4])
print(a[:-5])
print(a[:-1])

#Reverse a list items
a=["Naveen","Chanty","Srikanth"]
reverse=a[::-1]
print(reverse)

b=["Naveen","Chanty","Ananth"]
reverse=b[::1]
print(reverse)

c=["Naveen","Chanty","Gopi"]
reverse=c[::2]
print(reverse)
reverse=c[::-2]
print(reverse)
reverse=c[::3]
print(reverse)

