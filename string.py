#String
#Implicit
name="Naveen"
print(name)
print(type(name))
#Explicit
name=str("Naveen")
print(name)
print(type(name))
#Data type/Annotation
name:str="Naveen"
print(name)
print(type(name))

#Boolean
a=str(True)
print(a)
print(type(a))

#Octal
a=str(0o34565)
print(a)
print(type(a))

#Binery
a=str(0b10101010)
print(a)
print(type(a))

#Hexadecimal
a=str(0xdad)
print(a)
print(type(a))

#Binery
a=bin(0b10101010)
print(a)
print(type(a))

a=oct(0o10101010)
print(a)
print(type(a))

a=hex(0x10101010)
print(a)
print(type(a))








