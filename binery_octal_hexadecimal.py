#Number System

#binery system
#implecit
a=0b1010
print(a)
print(type(a))

b=0b1010101
print(b)
c=0b101010
print(c)
d=0b10101010
print(d)
print(type(d))

#explicit
a=bin(0b1010)
print(a)
print(type(a))
b=bin(0b10101010101010101)
print(b)
#datatype annotation
a:bin=0b1010
print(a)
print(type(a))

#octal number system
#impecit
a=0o1234
print(a)
print(type(a))
#explecit
a=oct(0o1234)
print(a)
print (type(a))
#data type annotation
a:oct=(0o1234)
print(a)
print(type(a))

#Hexa decimal
#implecit
a=0Xface
print(a)
print(type(a))
#explecit
a=hex(0X12ef)
print(a)
print(print(a))
#data annotation
a:hex=0X1234
